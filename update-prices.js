async function sleep(ms){ return new Promise(r=>setTimeout(r,ms)); }

async function fetchAll() {
  let all=[], page=1;
  while(true){
    const r = await fetch(`https://${SHOP}/products.json?limit=250&page=${page}`);
    const d = await r.json();
    all = all.concat(d.products);
    if(d.products.length < 250) break;
    page++;
  }
  all.sort((a,b)=>b.variants[0].id - a.variants[0].id);
  return all;
}

async function main(){
  const products = await fetchAll();
  console.log(`Fetched ${products.length} products`);
  let ok=0, fail=0;
  for(let i=0; i<products.length; i++){
    const p = products[i];
    const vid = p.variants[0].id;
    const newPrice = PRICES[i];
    if(!newPrice){ console.log(`  skip ${i} — no price`); continue; }
    const res = await fetch(`${API}/variants/${vid}.json`, {
      method: 'PUT',
      headers: HEADERS,
      body: JSON.stringify({ variant: { id: vid, price: newPrice.toFixed(2) } })
    });
    if(res.ok){ ok++; process.stdout.write('.'); }
    else { fail++; const e=await res.json(); console.log(`\n  FAIL ${i}: ${JSON.stringify(e)}`); }
    await sleep(520);
  }
  console.log(`\nDone: ${ok} updated, ${fail} failed`);
}
main();
